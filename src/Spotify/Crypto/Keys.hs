-- | Handshake key derivation and the server-signature check.
--
-- After Diffie–Hellman both sides hold a shared secret. The login challenge and
-- the two Shannon channel keys are derived from it (and from every handshake
-- byte seen so far) with HMAC-SHA1. Separately, the server's public key @gs@ is
-- verified against @gs_signature@ using Spotify's fixed 2048-bit RSA key, which
-- is what stops a man-in-the-middle from impersonating an access point.
module Spotify.Crypto.Keys
  ( HandshakeKeys (..)
  , computeKeys
  , hmacSha1
  , verifyServerSignature
  ) where

import           Crypto.Hash.Algorithms     (SHA1 (..))
import           Crypto.MAC.HMAC            (HMAC, hmac, hmacGetDigest)
import qualified Crypto.PubKey.RSA.PKCS15   as PKCS15
import           Crypto.PubKey.RSA.Types    (PublicKey (..))
import qualified Data.ByteArray            as BA
import           Data.Bits                 (shiftL, (.|.))
import           Data.ByteString           (ByteString)
import qualified Data.ByteString           as BS
import           Data.Word                 (Word8)

-- | HMAC-SHA1 as a plain @ByteString -> ByteString -> ByteString@.
hmacSha1 :: ByteString -> ByteString -> ByteString
hmacSha1 key msg = BA.convert (hmacGetDigest (hmac key msg :: HMAC SHA1))

-- | The three derived values: the login challenge HMAC and the per-direction
-- Shannon keys.
data HandshakeKeys = HandshakeKeys
  { hkChallenge :: !ByteString  -- ^ goes back in @ClientResponsePlaintext@
  , hkSendKey   :: !ByteString  -- ^ encrypts what we send
  , hkRecvKey   :: !ByteString  -- ^ decrypts what we receive
  } deriving (Eq, Show)

-- | Derive the handshake keys from the shared secret and the accumulated
-- handshake bytes (@ClientHello@ ‖ @APResponseMessage@, framing included).
computeKeys :: ByteString -> ByteString -> HandshakeKeys
computeKeys shared packets =
  HandshakeKeys
    { hkChallenge = hmacSha1 (BS.take 20 block) packets
    , hkSendKey   = slice 20 32
    , hkRecvKey   = slice 52 32
    }
  where
    block     = BS.concat [ hmacSha1 shared (packets <> BS.singleton i) | i <- [1 .. 5 :: Word8] ]
    slice o l = BS.take l (BS.drop o block)

-- | Verify the server's @gs@ against @gs_signature@ (RSA PKCS#1 v1.5, SHA-1).
verifyServerSignature
  :: ByteString  -- ^ @gs@ (the server's DH public key bytes)
  -> ByteString  -- ^ @gs_signature@
  -> Bool
verifyServerSignature gs signature =
  PKCS15.verify (Just SHA1) serverPublicKey gs signature

serverPublicKey :: PublicKey
serverPublicKey = PublicKey
  { public_size = 256
  , public_n    = bytesBE serverKey
  , public_e    = 65537
  }

bytesBE :: ByteString -> Integer
bytesBE = BS.foldl' (\acc b -> acc `shiftL` 8 .|. fromIntegral b) 0

